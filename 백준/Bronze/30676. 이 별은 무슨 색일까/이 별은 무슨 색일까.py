
num = int(input())

if 620 <= num and num <= 780:
    print("Red")
elif 590 <= num and num < 620:
    print("Orange")
elif 570 <= num and num < 590:
    print("Yellow")
elif 495 <= num and num < 570:
    print("Green")
elif 450 <= num and num < 495:
    print("Blue")
elif 425 <= num and num < 450:
    print("Indigo")
else:
    print("Violet")
